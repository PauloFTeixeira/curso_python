"""
Escreva um programa que escreva na tela, de 1 até 100m de 1 em 1, 3 vezes. A primeira deve usar a
estruta de repetição for, a segunda while e a terceira do while
"""
for n in range(1, 101):
    print(n)
num = 0
while num < 101:
    print(num)
    num = num + 1
i = 0
while True:
    print(i)
    i = i + 1
    if i == 101:
        break
